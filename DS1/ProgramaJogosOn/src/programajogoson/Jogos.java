package programajogoson;

public class Jogos {
    private String nome;
    private int indicacao;
    private int grauDeDificuldade;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIndicacao() {
        return indicacao;
    }

    public void setIndicacao(int indicacao) {
        this.indicacao = indicacao;
    }

    public int getGrauDeDificuldade() {
        return grauDeDificuldade;
    }

    public void setGrauDeDificuldade(int grauDeDificuldade) {
        this.grauDeDificuldade = grauDeDificuldade;
    }   
}
